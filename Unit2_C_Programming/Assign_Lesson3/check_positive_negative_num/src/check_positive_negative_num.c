#include <stdio.h>
#include <stdlib.h>

int main(void) {

	  float a=0;
	    //get a  values
		printf("\r\n Enter a Number= ");
		fflush(stdin);fflush(stdout);
		scanf("%f",&a);

	    if(a>0){
	    		printf("\r\n %4.2f is Positive",a);
	    }
	    else if(a<0){
	    	printf("\r\n %4.2f is Negative",a);
	    }
	    else{
	    	printf("\r\n You entered a Zero ");
	    }

	    return EXIT_SUCCESS;
}
